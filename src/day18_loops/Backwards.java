package day18_loops;

public class Backwards {
    public static void main(String[] args) {
    //starts from num 20 and go back to 1
        for (int i = 0; i <= 20; i++) {
            System.out.println(i);
        }
        System.out.println("________________");
        for (int i = 20 ; i >= 0 ; i--) {

            System.out.println(i);
        }



    }
}
